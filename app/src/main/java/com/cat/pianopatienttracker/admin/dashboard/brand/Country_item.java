package com.cat.pianopatienttracker.admin.dashboard.brand;

public class Country_item {
   private int id;
    private String name;

    public Country_item(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
